std::vector<float> norm_data::DiscTsnitGraph::resizeData(const std::vector<float>& data, int size)
{
	if (size <= 0 || size == data.size()) {
		return data;
	}

	auto resized = vector<float>(size);

	float incr = (float) data.size() / size;
	for (int i = 0; i < size; i++) {
		resized[i] = data[(int)(i * incr)];
	}

	return resized;
}


